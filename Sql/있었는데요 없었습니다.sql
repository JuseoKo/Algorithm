SELECT a.PRODUCT_CODE, sum(a.PRICE*b.SALES_AMOUNT) as SALES
from PRODUCT a, OFFLINE_SALE b
where a.PRODUCT_ID = b.PRODUCT_ID
group by PRODUCT_CODE
ORDER BY sales DESC, a.product_code